package com.example.javaweb_assignment.utils;

public class StrUtil {

    /**
     * 字符串转为Int
     * @param s 转换的字符串
     * @return 返回的int
     */
    public static int strToInt(String s){
        return Integer.parseInt(s);
    }

    /**
     * 字符串转化位Float
     * @param s 转换的字符串
     * @return 返回的float
     */
    public static float strToFloat(String s){
        return Float.parseFloat(s);
    }
}
